int hal_oled_open(void * i2c_driver) {
	oled_dev_t* pd = &oled_dev;
	if(!pd)
		return OLED_ERR_DEV_BAD_STATE;
	if(pd->used)
		return OLED_ERR_ALREADY_INIT;
	
	pd->i2c_driver = i2c_driver;
	if(pd->i2c_driver == NULL)
		return OLED_ERR_I2C_FAIL;
	//set_pins(oled_dev.i2c_driver, CFG_GPIO_4_0, CFG_GPIO_4_1);
	
	pd->isConnected = 0;
	pd->rssi_val = 0;	
	
	pd->displayInplay = 0;
	pd->displayRSSI = 0;
	pd->displayConnection = 0;
	
	pd->used = 1;
	pd->mutex = osMutexCreate(osMutex(oled_mutex));
	if (!pd->mutex)
		goto fail;
	pd->semaphore = osSemaphoreCreate(osSemaphore(oled_semaphore), 0);
	if (!pd->semaphore)
		goto fail;

#if CFG_PM_EN
	pd->pm.power_state = oled_power_state;
	pd->pm.power_down = oled_power_down;
	pd->pm.power_up = oled_power_up;
	pd->pm.arg = (void *)pd; 
	hal_pm_reg_mod(&pd->pm);
	pd->power_state = PM_DEEP_SLEEP;
#endif
	
	hal_oled_init();

	return OLED_ERR_OK;

fail:
	if (pd->mutex) {
		osMutexDelete(pd->mutex);
		pd->mutex = NULL;
	}
	if (pd->semaphore) {
		osSemaphoreDelete(pd->semaphore);	
		pd->semaphore = NULL;
	}
	return OLED_ERR_DEV_BAD_STATE;
}

int hal_oled_close(void) {
	oled_dev_t* pd = &oled_dev;
	if (!pd)
		return OLED_ERR_DEV_BAD_STATE;	
	if(!pd->used)
		return OLED_ERR_NOT_INIT;

	osMutexWait(pd->mutex, osWaitForever);
	osMutexRelease(pd->mutex);
	
	int result = hal_oled_clr_screen();
	//if(result != OLED_ERR_OK)
	//	return result;
	result = hal_oled_screen_power(0);
	//if(result != OLED_ERR_OK)
	//	return 0;

	
	if (pd->mutex) {
		osMutexDelete(pd->mutex);
		pd->mutex = NULL;
	}	
	if (pd->semaphore) {
		osSemaphoreDelete(pd->semaphore);
		pd->semaphore = NULL;
	}

#if CFG_PM_EN
	hal_pm_unreg_mod(&pd->pm);
#endif
	

	
	pd->used = 0;
	
	return OLED_ERR_OK;
}

int hal_oled_screen_power(uint8_t on) {
	oled_dev_t* pd = &oled_dev;
	if (!pd)
		return OLED_ERR_DEV_BAD_STATE;	
	if(!pd->used)
		return OLED_ERR_NOT_INIT;
	
	int result;
	if(on == 1) {
		result = oled_wr_cmd(0xaf);		//--turn on oled panel
		if(result != I2C_ERR_OK)
			return result;
	}
	else if(on == 0) {
		result = oled_wr_cmd(0xae);		//--turn on oled panel
		if(result != I2C_ERR_OK)
			return result;
	}
	else
		return OLED_ERR_INVALID_PARA;
	
	return OLED_ERR_OK;
}

int hal_oled_clr_screen(void) {
	oled_dev_t* pd = &oled_dev;
	if (!pd)
		return OLED_ERR_DEV_BAD_STATE;	
	if(!pd->used)
		return OLED_ERR_NOT_INIT;
	
	pd->displayInplay = 0;
	pd->displayRSSI = 0;
	pd->displayConnection = 0;
		
	osMutexWait(pd->mutex, osWaitForever);
#if CFG_PM_EN
	pd->power_state = PM_SLEEP;
#endif
	
	int result = OLED_ERR_OK;
	unsigned char data[128] = {0};
	for(uint8_t y = 0; y < 8; y++) {
		result = oled_set_pos(0,y);
		data[0] = 0xb0 + y;
		data[1] = 0x01;
		data[2] = 0x10;
		result = oled_wr_data(data, 3);
		if(result != I2C_ERR_OK)
			goto fail;
		data[0] = 0x0;
		data[1] = 0x0;
		data[2] = 0x0;
		result = oled_wr_data(data, 128);
		if(result != I2C_ERR_OK)
			goto fail;
	}


fail:
	
	osMutexRelease(pd->mutex);
#if CFG_PM_EN
	pd->power_state = PM_DEEP_SLEEP;
#endif	
	return result;
}

