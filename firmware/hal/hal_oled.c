/* OLED write Data */
static __inline int8_t oled_wr_data(unsigned char *IIC_Data, uint16_t len) {
	uint8_t reg[len + 1];
	reg[0] = WR_OP_DATA;
	for(int i = 0; i < len; i++)
		reg[i+1] = IIC_Data[i];
	int result = hal_mi2c_write(oled_dev.i2c_driver, I2C_SPEED_100K, OLED_I2C_ADDRESS, reg, len + 1, 2000);
	if(result != I2C_ERR_OK)
		return OLED_ERR_I2C_FAIL;
	return OLED_ERR_OK;
}

/* OLED write Command */
static __inline int8_t oled_wr_cmd(unsigned char IIC_Command) {	
	uint8_t reg[2];
	reg[0] = WR_OP_CMD;
	reg[1] = IIC_Command;
	int result = hal_mi2c_write(oled_dev.i2c_driver, I2C_SPEED_100K, OLED_I2C_ADDRESS, reg, 2, 2000);
	if(result != I2C_ERR_OK)
		return OLED_ERR_I2C_FAIL;
	return OLED_ERR_OK;
}

/*********************OLED Set Position************************************/
static __inline int8_t oled_set_pos(unsigned char x, unsigned char y) {
	int result = oled_wr_cmd(0xb0+y);
	if(result != I2C_ERR_OK)
		return result;
	result = oled_wr_cmd(((x & 0xf0) >> 4) | 0x10);
	if(result != I2C_ERR_OK)
		return result;
	result = oled_wr_cmd((x & 0x0f) | 0x01);
	if(result != I2C_ERR_OK)
		return result;
	return OLED_ERR_OK;
}

